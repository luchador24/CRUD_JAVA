    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        // TODO add your handling code here:
        if (carNom.getText().trim().equals("") && carPai.getText().trim().equals("") && carCar.getText().trim().equals("") && carCat.getText().trim().equals(""))
        JOptionPane.showMessageDialog(rootPane, "No puede dejar cuadros en blanco...");
        else
        {
            if (JOptionPane.showConfirmDialog(rootPane, "Guardar esta información?", "Carretera", JOptionPane.YES_NO_OPTION)==0)
            {
                try
                {
                    con=conectar.getConexion();
                    String insercion = "insert into carretera values(?,?,?,?,?)";
                    ps = (PreparedStatement) con.prepareStatement(insercion);
                    ps.setString(1, carId.getText().trim());
                    ps.setString(2, carNom.getText().toUpperCase().trim());
                    ps.setString(3, carPai.getText().toUpperCase().trim());
                    ps.setString(4, carCar.getText().trim());
                    ps.setString(5, carCat.getText().trim());
                    ps.executeUpdate();
                    con.close();
                    JOptionPane.showMessageDialog(rootPane, "Registro guardado...");
                    alCancelar();

                } catch (Exception e)
                {
                    JOptionPane.showMessageDialog(rootPane, e.toString());
                }
            }
        }
    }//GEN-LAST:event_cmdGuardarActionPerformed

