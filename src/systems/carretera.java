    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        // TODO add your handling code here:
        boolean encontrado = false;
        if(carId.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Ingrese un numero de ID");
        }else{
            String busqueda = "select * from Carretera where idCarretera=" + carId.getText().trim();
            try
            {
                con=conectar.getConexion();
                st=(Statement) con.createStatement();
                st2=(Statement) con.createStatement();
                rs=st.executeQuery(busqueda);
                while (rs.next())
                {
                    encontrado = true;
                    cmdModificar.setEnabled(true);
                    cmdGuardar.setEnabled(false);
                    cmdBorrar.setEnabled(true);
                    carNom.setText(rs.getString("nombre"));
                    carPai.setText(rs.getString("pais"));
                    carCar.setText(rs.getString("numCarriles"));
                    carCat.setText(rs.getString("idcategoria"));
                    carId.setText(rs.getString("idCarretera"));
                    rs2=st2.executeQuery("Select * from categoria where idcategoria="+carId.getText().trim());
                    while (rs2.next())
                    {
                        carCat.setText(rs2.getString("idcategoria"));

                    }                    
                }
                con.close();
                cmdCancelar.setEnabled(true);
                cmdBuscar.setEnabled(false);
                carId.setEnabled(false);
                carNom.setEnabled(true);
                carPai.setEnabled(true);
                carCar.setEnabled(true);
                if (encontrado == false)
                    cmdGuardar.setEnabled(true);
                    cmdBuscarCat.setEnabled(true);
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(rootPane, e.toString());
            }
        }

    }//GEN-LAST:event_cmdBuscarActionPerformed

