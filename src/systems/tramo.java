    private void cmdBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBorrarActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(rootPane, "Seguro quiere eliminar este registro?", "Tramo", JOptionPane.YES_NO_OPTION)==0)
        {
            try
            {
                con=conectar.getConexion();
                String eliminacion = "delete from tramo where idtramo='" + traId.getText().trim() + "'" ;
                ps = (PreparedStatement) con.prepareStatement(eliminacion);
                ps.executeUpdate();
                con.close();
                JOptionPane.showMessageDialog(rootPane, "Registro eliminado...");
                alCancelar();

            } catch (Exception e)
            {
                JOptionPane.showMessageDialog(rootPane, e.toString());
            }
        }
    }//GEN-LAST:event_cmdBorrarActionPerformed

    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        // TODO add your handling code here:
        boolean encontrado = false;
        if(traId.getText().trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Ingrese un numero de ID");
        }else{
            String busqueda = "select * from tramo where idtramo=" + traId.getText().trim();
            try
            {
                con=conectar.getConexion();
                st=(Statement) con.createStatement();
                rs=st.executeQuery(busqueda);
                while (rs.next())
                {
                    encontrado = true;
                    cmdModificar.setEnabled(true);
                    cmdGuardar.setEnabled(false);
                    cmdBorrar.setEnabled(true);
                    cmdBuscarCat.setEnabled(true);
                    traIni.setText(rs.getString("kmInicioCarretera"));
                    traFin.setText(rs.getString("kmFinCarretera"));
                    traCar.setText(rs.getString("idCarretera"));
                }
                con.close();
                cmdCancelar.setEnabled(true);
                cmdBuscar.setEnabled(false);
                traId.setEnabled(false);
                traIni.setEnabled(true);
                traFin.setEnabled(true);              
                if (encontrado == false){
                    cmdBuscarCat.setEnabled(true);
                    cmdGuardar.setEnabled(true);                    
                }

            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(rootPane, e.toString());
            }
        }

    }//GEN-LAST:event_cmdBuscarActionPerformed

